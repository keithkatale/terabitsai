#!/usr/bin/env node
/**
 * Sync external skill repos from vendor/ into skills/external/ and regenerate index.
 *
 * Usage:
 *   node scripts/sync-external-skills.mjs
 *
 * Prerequisites: vendor/finance-skills and vendor/claude-trading-skills cloned.
 */
import { execSync } from "child_process";
import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from "fs";
import { join, dirname, basename } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const vendorFinance = join(root, "vendor/finance-skills");
const vendorClaude = join(root, "vendor/claude-trading-skills");
const externalRoot = join(root, "skills/external");

function run(cmd) {
  execSync(cmd, { cwd: root, stdio: "inherit" });
}

function ensureVendor() {
  if (!existsSync(vendorFinance)) {
    run(`git clone --depth 1 https://github.com/himself65/finance-skills.git vendor/finance-skills`);
  }
  if (!existsSync(vendorClaude)) {
    run(`git clone --depth 1 https://github.com/tradermonty/claude-trading-skills.git vendor/claude-trading-skills`);
  }
}

function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  const meta = {};
  for (const line of match[1].split("\n")) {
    const m = line.match(/^(\w[\w-]*):\s*(.+)$/);
    if (m) meta[m[1]] = m[2].trim().replace(/^["']|["']$/g, "");
  }
  return meta;
}

function walkSkillMd(dir, source, entries = []) {
  if (!existsSync(dir)) return entries;
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) {
      if (name.startsWith("_")) continue;
      walkSkillMd(full, source, entries);
    } else if (name === "SKILL.md") {
      const rel = dirname(full).replace(join(externalRoot, source) + "/", "");
      const content = readFileSync(full, "utf8");
      const fm = parseFrontmatter(content);
      entries.push({
        id: fm.name || basename(dirname(full)),
        name: fm.name || basename(dirname(full)),
        source,
        path: join(source, rel),
        description: (fm.description || "").slice(0, 400),
        category: rel.split("/")[0] || "general",
      });
    }
  }
  return entries;
}

function main() {
  ensureVendor();

  run(`mkdir -p "${externalRoot}/finance-skills" "${externalRoot}/claude-trading-skills/_workflows" "${externalRoot}/claude-trading-skills/_agents"`);
  run(`rsync -a vendor/claude-trading-skills/skills/ skills/external/claude-trading-skills/`);
  run(`rsync -a vendor/claude-trading-skills/workflows/ skills/external/claude-trading-skills/_workflows/`);
  run(`rsync -a vendor/claude-trading-skills/agents/ skills/external/claude-trading-skills/_agents/`);

  run(`find vendor/finance-skills/plugins -name SKILL.md -print0 | while IFS= read -r -d '' f; do
    plugin=$(echo "$f" | sed 's|.*/plugins/||;s|/skills/.*||')
    skill=$(basename "$(dirname "$f")")
    dest="skills/external/finance-skills/$plugin/$skill"
    mkdir -p "$dest"
    rsync -a "$(dirname "$f")/" "$dest/"
  done`);

  run(`rsync -a vendor/finance-skills/opencli-plugins/ skills/external/finance-skills/opencli-plugins/`);

  const claude = walkSkillMd(join(externalRoot, "claude-trading-skills"), "claude-trading-skills");
  const finance = walkSkillMd(join(externalRoot, "finance-skills"), "finance-skills");
  const all = [...claude, ...finance];

  const yaml = [
    "# Auto-generated by scripts/sync-external-skills.mjs",
    `# Last sync: ${new Date().toISOString()}`,
    `version: "1.0.0"`,
    "",
    "external_skills:",
    ...all.map((s) => {
      const desc = (s.description || "").replace(/\n/g, " ").replace(/"/g, '\\"');
      return [
        `  - id: ${s.id}`,
        `    source: ${s.source}`,
        `    path: ${s.path}`,
        `    category: ${s.category}`,
        `    status: reference`,
        `    description: >`,
        `      ${desc || s.id}`,
      ].join("\n");
    }),
    "",
  ].join("\n");

  writeFileSync(join(root, "skills/external-skills-index.yaml"), yaml);
  console.log(`Indexed ${all.length} external skills (${claude.length} claude, ${finance.length} finance)`);
}

main();
